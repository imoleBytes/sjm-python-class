
weather = input("Enter weather (sunny, rainy, cloudy, clear): ")

if weather == "sunny":
    print("today is sunny, go out with sunshades")
elif weather == "rainy":
    print("it is cold outside, take your umbrella")
elif weather == "cloudy":
    print("it might rain")
elif weather == "clear":
    print("Have a goood day, the weather is clear!")

else:
    print("invalid weather")
    
